    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        String computerNetworks = jTextField2.getText();
        String operatingSystem = jTextField3.getText();
        String DBMS = jTextField4.getText();
        String DSA = jTextField5.getText();
        String OOPs = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root", "BASITkaw123@");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM student WHERE rollNo ='"+rollNo+"'");
            if(rs.next()){
                st.executeUpdate("INSERT INTO result (rollNo, computerNetworks, operatingSystem, DBMS, DSA, OOPs) VALUES ('"+rollNo+"','"+computerNetworks+"','"+operatingSystem+"','"+DBMS+"','"+DSA+"','"+OOPs+"')");
                JOptionPane.showMessageDialog(null, "Result entered successfully");
                setVisible(false);
                new insertstudent().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student not registered with given Roll No");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

